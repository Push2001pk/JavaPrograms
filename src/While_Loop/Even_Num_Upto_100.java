package While_Loop;

public class Even_Num_Upto_100 
{

	public static void main(String[] args) 
	{  
		System.out.println("Even no upto 100...............");
		System.out.println("================================");
		
		
		
		int i=1;
		while(i<=100)
		{
			if(i%2==0)
			{
				System.out.println(i);
			}
			i++;
			
		}
	}

}
